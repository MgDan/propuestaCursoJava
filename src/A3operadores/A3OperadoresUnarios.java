package A3operadores;

public class A3OperadoresUnarios {
    public static void main(String[] args) {
        /* Operadores Unarios
        *
        * Operan con el signo del valor numerico con el que estan trabajando
        *
        * Hay 2 + y -
        *
        * */
        
        int i = -5;
        int j = +i; // es como multiplicar por 1 positivo

        System.out.println("j = " + j);
        
        int k = -i; // lo mismo pero en negativo
        System.out.println("k = " + k);
        
        
    }
}
